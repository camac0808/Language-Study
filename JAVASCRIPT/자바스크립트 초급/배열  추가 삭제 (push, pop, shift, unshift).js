// 배열 뒤쪽 추가 push 뒤쪽 삭제 pop
var arr = [1, 2, 3];
arr.push(4); // 1, 2, 3, 4
arr.pop(); // 1, 2, 3

// 배열 앞쪽 추가 unshift 앞쪽 삭제 shift
arr.unshift(0); // 0, 1, 2, 3
arr.shift(); // 1, 2, 3


